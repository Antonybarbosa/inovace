        SELECT
            SUM(I.VALOR * I.TIPIMP)
        FROM
            TGFIMF I
        WHERE
            I.NUFIN = TGFFIN.NUFIN
    ),0) + NVL(TGFFIN.VLRMULTANEGOC,0) + NVL(TGFFIN.VLRJURONEGOC,0) - NVL(TGFFIN.VLRMULTALIB,0) - NVL(TGFFIN.VLRJUROLIB,0) + NVL(TGFFIN.VLRVARCAMBIAL
,0) ) * NVL(TGFFIN.RECDESP,0) ) AS VLRLIQUIDO,
    ( NVL(TGFFIN.VLRDESDOB,0) + NVL(TGFFIN.VLRJURO,0) + NVL(TGFFIN.VLRMULTA,0) - NVL(TGFFIN.VLRDESC,0) ) AS VLRTOTALCALC,
