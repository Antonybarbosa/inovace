        CASE
            WHEN TGFFIN.NUNOTA IS NOT NULL
                 AND TGFFIN.NUNOTA > 0 THEN (
                SELECT
                    CAB.RATEADO
                FROM
                    TGFCAB CAB
                WHERE
                    CAB.NUNOTA = TGFFIN.NUNOTA
            )
            ELSE 'N'
        END
    ) AS RATEADOCAB,
