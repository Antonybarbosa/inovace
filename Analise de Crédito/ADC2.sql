                CASE
                    WHEN TGFFIN.NUBCO IS NULL THEN TGFFIN.VLRBAIXA
                    ELSE (
                        SELECT
                            SUM(F.VLRBAIXA)
                        FROM
                            TGFFIN F
                        WHERE
                            F.NUBCO = TGFFIN.NUBCO
                    )
                END
            ELSE 0
        END
    ) AS VLRTROCO
